package jigsaw.wn;

/**
 *
 * @author  Basile Pierpaolo and Grieco Franco
 */
public class WnNode {
    
    /**
     * Synset offset
     */
    private String offset;

    @Override
    public boolean equals(Object obj) {
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final WnNode other = (WnNode) obj;
        if ((this.getOffset() == null) ? (other.getOffset() != null) : !this.offset.equals(other.offset)) {
            return false;
        }
        return true;
    }

    @Override
    public int hashCode() {
        int hash = 3;
        hash = 89 * hash + (this.getOffset() != null ? this.getOffset().hashCode() : 0);
        return hash;
    }
    
    
    
    /**
     * Synset depth
     */
    private int depth;
    
    /** Creates a new instance of WnNode */
    public WnNode() {
    }
    
    

    @Override
    public String toString() {
        return this.getOffset()+" "+this.getDepth();
    }

    /**
     * @return the offset
     */
    public String getOffset() {
        return offset;
    }

    /**
     * @param offset the offset to set
     */
    public void setOffset(String offset) {
        this.offset = offset;
    }

    /**
     * @return the depth
     */
    public int getDepth() {
        return depth;
    }

    /**
     * @param depth the depth to set
     */
    public void setDepth(int depth) {
        this.depth = depth;
    }
    
    
}
